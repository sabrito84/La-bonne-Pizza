const tabs = document.querySelectorAll("[data-tab-target]");
const tabContents = document.querySelectorAll("[data-tab-content]");

tabs.forEach((tab) => {
  tab.addEventListener("click", () => {
    const target = document.querySelector(tab.dataset.tabTarget);

    // Remove active class from all tab contents
    tabContents.forEach((tabContent) => {
      tabContent.classList.remove("active");
    });

    // Add active class to the target tab content
    target.classList.add("active");

    // Remove active class from all tab buttons
    tabs.forEach((t) => {
      t.classList.remove("active-btn");
    });

    // Add active class to the clicked tab button
    tab.classList.add("active-btn");

    // Clear existing products to prevent duplication
    clearProducts();

    // Generate products for each tab as needed
    if (tab.dataset.tabTarget === "#Pizza02") {
      makePizza();
      makePizza2();
    } else if (tab.dataset.tabTarget === "#Hamburgeur") {
      makeHamburgeur();
    } else if (tab.dataset.tabTarget === "#Sandwitch") {
      makeSandwitch();
    } else if (tab.dataset.tabTarget === "#Boisson") {
      makeBoisson();
    } else if (tab.dataset.tabTarget === "#Dessert") {
      makeDessert();
    }
  });
});

const clearProducts = () => {
  const productContainers = document.querySelectorAll(".grid_for_product");
  productContainers.forEach((container) => {
    container.innerHTML = "";
  });
};

const makePizza = () => {
  ProductPizza.forEach((product) => {
    let getPizza = document.querySelector("#Pizza");
    let newDiv = document.createElement("div");
    newDiv.classList.add("product_main");
    newDiv.innerHTML = `
      <div class='interne_product_picture'>
        <img class='image_product' src='${product.image}'></img>
      </div>
      <p class='title_product'>${product.name}</p>
      <p class='description_product'>${product.ingredient}</p>
      <p class='price_product'>${product.price} €</p>
    `;
    getPizza.appendChild(newDiv);
  });
};

